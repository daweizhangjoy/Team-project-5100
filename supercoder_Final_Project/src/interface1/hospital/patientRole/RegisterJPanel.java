    private void populatePetList() {
        DefaultTableModel model = (DefaultTableModel) petTable.getModel();
        
        model.setRowCount(0);
        
        for (Pet pet : customer.getPetDirectory().getPetList())
        {
            Object[] row = new Object[2];
            row[0] = pet;
            row[1] = pet.getPetName();
            model.addRow(row);
        }
    }
