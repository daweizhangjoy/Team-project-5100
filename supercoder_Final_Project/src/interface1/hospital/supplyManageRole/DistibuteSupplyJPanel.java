    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        int delta = Integer.parseInt(quantityTextField.getText());
        
        if((delta<0) || (delta>supply.getSupplyQuantity()))
        {
            JOptionPane.showMessageDialog(null, "Supply not enough");
        }
        else
        {
            supply.setSupplyQuantity(supply.getSupplyQuantity() - delta);
            JOptionPane.showMessageDialog(null, "change committed");
            
            userProcessContainer.remove(this);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.previous(userProcessContainer);
        }
    }//GEN-LAST:event_submitButtonActionPerformed

